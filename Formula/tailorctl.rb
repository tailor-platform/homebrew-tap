      url "https://github.com/tailor-platform/tailorctl/releases/download/v0.9.14/tailorctl_linux_v0.9.14_arm64.tar.gz"
      sha256 "4efdc7cafad6cf9aba9f4fceb7773669e54101322f196684b4d3f1b542cd831c"

